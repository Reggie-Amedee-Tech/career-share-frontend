const STATE_BY_CODE = new Map<string, (typeof US_STATES)[number]>(
  US_STATES.map((state) => [state.code, state]),
);
const STATE_BY_NAME = new Map(
  US_STATES.map((state) => [state.name.toLowerCase(), state]),
);

export function resolveInitialStateValue(location: string) {
  const trimmed = location.trim();
  if (!trimmed) {
    return "";
  }

  if (STATE_BY_NAME.has(trimmed.toLowerCase())) {
    return STATE_BY_NAME.get(trimmed.toLowerCase())!.name;
  }

  const upper = trimmed.toUpperCase();
  if (STATE_BY_CODE.has(upper)) {
    return STATE_BY_CODE.get(upper)!.name;
  }

  const cityStateMatch = trimmed.match(/,\s*([A-Z]{2})\s*$/i);
  if (cityStateMatch) {
    const state = STATE_BY_CODE.get(cityStateMatch[1].toUpperCase());
    if (state) {
      return state.name;
    }
  }

  return trimmed;
}
